    public void showTextData(int index){
        if(data==null || data.size()==0){
            JOptionPane.showMessageDialog(null, "目前数据为空","系统提示",JOptionPane.WARNING_MESSAGE);
        }else{
            txtExam.setText(data.get(index).get(1));
            txtA.setText(data.get(index).get(2));
            txtB.setText(data.get(index).get(3));
            txtC.setText(data.get(index).get(4));
            txtD.setText(data.get(index).get(5));
        }
        //调用锁定文本框的方法
        lockText();
    }
    
   
   
    private void setButton(char c) {  //选择选项
        if (c == 'A') {
            A.setSelected(true);
        } else if (c == 'B') {
            B.setSelected(true);
        } else if (c == 'C') {
            C.setSelected(true);
        } else if (c == 'D') {
            D.setSelected(true);
        } 
    }
    
    
