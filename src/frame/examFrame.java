    private void showChoiceData(int index) {
        String choice = QuestionUtil.getChoiceAnswerItem(index);
        if(choice != null) {
            switch (choice) {
                case "" : Null.setSelected(true);break;
                case "A" : A.setSelected(true);break;
                case "B" : B.setSelected(true);break;
                case "C" : C.setSelected(true);break;
                case "D" : D.setSelected(true);break;
            }
        }
    }
